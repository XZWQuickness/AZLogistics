public class WeatherBean {
    private String province;

    public String getProvince() { return this.province; }

    public void setProvince(String province) { this.province = province; }

    private String city;

    public String getCity() { return this.city; }

    public void setCity(String city) { this.city = city; }

    private String adcode;

    public String getAdcode() { return this.adcode; }

    public void setAdcode(String adcode) { this.adcode = adcode; }

    private String weather;

    public String getWeather() { return this.weather; }

    public void setWeather(String weather) { this.weather = weather; }

    private String temperature;

    public String getTemperature() { return this.temperature; }

    public void setTemperature(String temperature) { this.temperature = temperature; }

    private String winddirection;

    public String getWinddirection() { return this.winddirection; }

    public void setWinddirection(String winddirection) { this.winddirection = winddirection; }

    private String windpower;

    public String getWindpower() { return this.windpower; }

    public void setWindpower(String windpower) { this.windpower = windpower; }

    private String humidity;

    public String getHumidity() { return this.humidity; }

    public void setHumidity(String humidity) { this.humidity = humidity; }

    private String reporttime;

    public String getReporttime() { return this.reporttime; }

    public void setReporttime(String reporttime) { this.reporttime = reporttime; }
}
